google.load("visualization", "1", {packages:["table"]});
google.setOnLoadCallback(drawTable);
function drawTable() {
  var data = new google.visualization.DataTable();

  data.addColumn('string', 'Product');
  data.addColumn('string', 'Informatie over producten');
  data.addRows([
    ['Wortelen','<a href="wortelen.html">Meer informatie</a>'],
    ['Tomaten','<a href="tomaten.html">Meer informatie</a>'],
    ['Uien','<a href="uien.html">Meer informatie</a>'],
    ['Komkommers','<a href="komkommers.html">Meer informatie</a>'],
    ['Paprika','<a href="paprika.html">Meer informatie</a>']
  ]);

  var table = new google.visualization.Table(document.getElementById('table_div'));

  table.draw(data, {showRowNumber: false, allowHtml: true, width: '100%', height: '100%'});
}



